#import "CustomPhoneGapViewController.h"

@implementation CustomPhoneGapViewController

@synthesize name, delegate, reference;

// return a new autoreleased UUID string
- (NSString *)generateUuidString
{
    // create a new UUID which you own
    CFUUIDRef uuid = CFUUIDCreate(kCFAllocatorDefault);
    
    // create a new CFStringRef (toll-free bridged to NSString)
    // that you own
    NSString *uuidString = (NSString *)CFUUIDCreateString(kCFAllocatorDefault, uuid);
    
    // transfer ownership of the string
    // to the autorelease pool
    [uuidString autorelease];
    
    // release the UUID
    CFRelease(uuid);
    
    return uuidString;
}

- (id)init{
    
    if(self = [super init])
    {
        self.name = [self generateUuidString];
    }
    
    return self;    
}

- (void)viewDidDisappear:(BOOL)animated
{
    if (!self.parentViewController && self.delegate)
        [delegate phoneGapControllerPopped:self];        
}

- (void)finishedLoading {
    
}

- (void)dealloc
{
    self.name = nil;
    self.reference = nil;
    [super dealloc];
}

@end
