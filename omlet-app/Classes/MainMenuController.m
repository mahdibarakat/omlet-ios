#import "MainMenuController.h"

@interface MainMenuController ()
@property (nonatomic, retain) NSMutableArray* menuItemsFlags; 
@end

@implementation MainMenuController
@synthesize menuItems, delegate, menuItemsFlags;

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)dealloc {
    self.menuItemsFlags = nil;
    [menuItems release];
    [super dealloc];
}

- (void)setMenuItems:(NSArray *)_menuItems
{
    [menuItems release];
    menuItems = [_menuItems copy];
    
    self.menuItemsFlags = [NSMutableArray arrayWithCapacity:[menuItems count]];
    
    for (id item in menuItems) {
        [self.menuItemsFlags addObject:[NSNumber numberWithBool:YES]];
    }
}

- (void) setButtonEnabled:(NSUInteger) buttonIndex isEnabled:(BOOL)enabled
{
    [self.menuItemsFlags replaceObjectAtIndex:buttonIndex withObject:[NSNumber numberWithBool:enabled]]; // buttonIndex] = enabled;
    [self.tableView cellForRowAtIndexPath:[NSIndexPath indexPathForRow:buttonIndex inSection:0]].userInteractionEnabled = enabled;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    CGRect rect = [UIScreen mainScreen].applicationFrame;
    rect.size.width -= 80;
